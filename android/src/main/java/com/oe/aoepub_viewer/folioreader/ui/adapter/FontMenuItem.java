package com.oe.aoepub_viewer.folioreader.ui.adapter;

public class FontMenuItem {
    String title;
    String font;

    public FontMenuItem(String title, String font) {
        this.title = title;
        this.font = font;
    }

    public String getFont() {
        return font;
    }

    public String getTitle() {
        return title;
    }
}
